import pandas as pd
import duckdb
import matplotlib.pyplot as plt
import seaborn as sns
from pathlib import Path
from src.common.analysis import Analysis

class WhaleHunter(Analysis):
    name = "whale_hunter_pro"
    description = "Analyzes large trades ($1k+) and visualizes market concentration."

    def run(self):
        # 1. Setup Paths
        base_dir = Path(__file__).parent.parent.parent.parent
        trades_path = base_dir / "data" / "polymarket" / "trades" / "*.parquet"
        markets_path = base_dir / "data" / "polymarket" / "markets" / "*.parquet"

        # Check for data presence
        if not list(base_dir.glob("data/polymarket/trades/*.parquet")):
            print("❌ No data found. Please run 'make index' first.")
            return None, {}

        # 2. Advanced SQL Query
        # We calculate the trade size and join it with market names in one go
        query = f"""
            SELECT
                m.question as market_name,
                t.side,
                t.price,
                t.size,
                (t.price * t.size) as usd_value,
                t.timestamp
            FROM read_parquet('{trades_path}') t
            JOIN read_parquet('{markets_path}') m ON t.market_id = m.id
            WHERE usd_value >= 1000
            ORDER BY usd_value DESC
        """

        with self.progress("Hunting whales and calculating market impact..."):
            con = duckdb.connect()
            df = con.execute(query).df()

        if df.empty:
            print("No whale activity detected in this dataset.")
            return None, {}

        # 3. Generate Insight: Top 10 Markets by Whale Volume
        whale_summary = df.groupby('market_name')['usd_value'].sum().sort_values(ascending=False).head(10).reset_index()

        # 4. Create Visualization
        fig = self.create_visualization(whale_summary)

        # 5. Save everything
        # This saves the CSV, JSON, and the PNG chart to /output
        self.save(df, "whale_trades_detailed")
        plot_path = base_dir / "output" / "whale_concentration.png"
        fig.savefig(plot_path, dpi=300, bbox_inches='tight')

        print(f"\n✅ Analysis Complete!")
        print(f"📊 Chart saved to: {plot_path}")
        print(f"📂 Data saved to: output/whale_trades_detailed.csv")

        return fig, df

    def create_visualization(self, data):
        """Creates a professional bar chart of whale activity."""
        sns.set_theme(style="whitegrid")
        fig, ax = plt.subplots(figsize=(12, 7))

        # Shorten long market names for the chart labels
        data['short_name'] = data['market_name'].apply(lambda x: (x[:45] + '...') if len(x) > 45 else x)

        plot = sns.barplot(
            data=data,
            y='short_name',
            x='usd_value',
            palette="mako",
            ax=ax
        )

        ax.set_title("Top 10 Markets by Whale Volume (Trades > $1,000)", fontsize=16, fontweight='bold')
        ax.set_xlabel("Total Whale Volume (USD)", fontsize=12)
        ax.set_ylabel("Market Question", fontsize=12)

        # Add dollar signs to x-axis
        import matplotlib.ticker as ticker
        ax.xaxis.set_major_formatter(ticker.StrMethodFormatter('${x:,.0f}'))

        plt.tight_layout()
        return fig

if __name__ == "__main__":
    # Allows for standalone testing
    WhaleHunter().run()